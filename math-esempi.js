// ESEMPI SEMPLICI CON MATH

// MAX e MIN
console.log("MAX tra 5, 12, 8:", Math.max(5, 12, 8));
console.log("MIN tra 5, 12, 8:", Math.min(5, 12, 8));

// RADICE QUADRATA e POTENZA
console.log("RADICE QUADRATA di 16:", Math.sqrt(16));
console.log("3 elevato alla 4:", Math.pow(3, 4));

// ARROTONDAMENTI
console.log("FLOOR di 4.7:", Math.floor(4.7));
console.log("CEIL di 4.7:", Math.ceil(4.7));
console.log("ROUND di 4.5:", Math.round(4.5));

// NUMERO CASUALE
console.log("NUMERO CASUALE tra 0 e 1:", Math.random());

// NUMERO CASUALE INTERO tra 1 e 10
console.log("NUMERO CASUALE INTERO tra 1 e 10:", Math.floor(Math.random() * 10) + 1);
